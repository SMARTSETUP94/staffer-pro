/** Normalise une chaîne (lowercase + suppression accents). */
export function normalizePrenom(s: string): string {
  return s
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .trim();
}

/** Renvoie les prénoms fêtés à la date donnée (MM-DD). */
export function getSaintsForDate(date: Date): string[] {
  const key = `${String(date.getMonth() + 1).padStart(2, "0")}-${String(date.getDate()).padStart(2, "0")}`;
  return SAINTS_FR[key] ?? [];
}
